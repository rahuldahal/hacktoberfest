#include <iostream>
using namespace std;

int main() {
    // Step 1: Accepting Input
    float num1, num2;
    cout << "Enter two numbers: ";
    cin >> num1 >> num2;

    // Step 2: Performing division
    float div = num1 / num2;

    // Step 3: Displaying the Result
    cout << "The division of two number is: " << div << endl;

    return 0;
}
